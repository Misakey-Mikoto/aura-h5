// 根据分数和项目获取结果的函数
function getSkinResult(item, score) {
  // 验证输入参数
  if (!skinData[item]) {
    throw new Error(`未找到项目: ${item}，请检查项目名称是否正确`);
  }
  
  if (score < 0 || score > 10) {
    throw new Error("分数必须在0-10之间");
  }
  
  const data = skinData[item];
  const { thresholds, descriptions, advice } = data;
  
  // 确定分数对应的级别
  let levelIndex;
  if (score <= thresholds[1]) {
    levelIndex = 0; // 0-3分
  } else if (score <= thresholds[2]) {
    levelIndex = 1; // 3-6分
  } else if (score <= thresholds[3]) {
    levelIndex = 2; // 6-8分
  } else {
    levelIndex = 3; // 8-10分
  }
  
  // 获取描述
  const description = descriptions[levelIndex];
  
  return {
    item: item,                   // 项目名称
    score: score,                 // 分数
    level: levelIndex,            // 等级索引 0-3 (0最差，3最好)
    description: description,     // 描述文本
    advice: advice,               // 护理建议
    // 为了方便使用，也可以返回HTML格式
    html: `
      <div class="skin-result">
        <h3>${item}检测结果</h3>
        <p><strong>得分:</strong> ${score}/10</p>
        <div class="description">
          <p>${description.replace(/\n/g, '</p><p>')}</p>
        </div>
        <div class="advice">
          <h4>护理建议:</h4>
          <p>${advice.replace(/\n/g, '</p><p>')}</p>
        </div>
      </div>
    `
  };
}


